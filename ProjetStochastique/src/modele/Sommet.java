package modele;

public class Sommet {
	
	private String name;
	private int x;
	private int y;
	
	
	public Sommet(String name, int x, int y) {
		this.name=name;
		this.x=x;
		this.y=y;
	}
	
	
	public String getName() {
		return name;
	}
	
	public String getX() {
		return name;
	}
	
	public String getY() {
		return name;
	}
	
	
	public void setX(int x) {
		this.x=x;
	}
	
	public void setY(int y) {
		this.y=y;
	}

}
